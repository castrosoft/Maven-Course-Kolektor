package ar.gob.rentascba;

public class Calculator{

    public Calculator(){
    }

    public int add(int a, int b){
        return a + b;
    }

    public int subs(int a, int b){
        return a - b;
    }

    public int mult(int a, int b){
        return a * b;
    }

    public int div(int a, int b){
        return a / b;
    }
}